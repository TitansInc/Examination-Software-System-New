    void readFileNames(String FileNames[]){
        try{
            URL url=new URL(getClass().getResource("/").getPath().substring(0, getClass().getResource("/").getPath().lastIndexOf("!")));
            System.out.println(url);
            File file=new File(url.getPath());
            JarInputStream zin = new JarInputStream(new FileInputStream(file));
            JarEntry entry;
            String temp;
            int i=0;
            while ((entry = zin.getNextJarEntry()) != null)
            {   
                if(entry.getName().contains("Questions/")){
                    temp=entry.getName().substring(entry.getName().lastIndexOf("/")+1).trim();
                    if(!temp.equals("")){
                        FileNames[i]=temp;
                        i++;
                    }
                }
                zin.closeEntry();
            }
            zin.close();
        }
        catch (Exception e)
        {
            e.printStackTrace();
        }
    }
    void sortFileNames(String[] arr){
        for(int j = 0; j < arr.length; j++) {       
            for(int i = j + 1; i < arr.length; i++) {
                int ith=Integer.valueOf(arr[i].substring(0, arr[i].indexOf("."))).intValue();
                int jth=Integer.valueOf(arr[j].substring(0, arr[j].indexOf("."))).intValue();
                if(ith<jth) {
                    String t = arr[j];
                    arr[j] = arr[i];
                    arr[i] = t;
                }
            }
        }
    }

    
