    void getQuestionType(){
        
       countagain=0;
       String[] QuestionPattern;
       if(!jTextPane2.getText().equals("")){
         String str=jTextPane2.getText().trim();
         String str2="";  
         int skipper=0;
         for(int i=0;i<str.length();i++){
             if(str.charAt(i)!='\n')
                 str2=str2+str.charAt(i);
         }
         str2=str2+",";
         for(int i=0;i<str2.length();i++){
             
             if(skipper==0 && str2.charAt(i)==','){
            	 countagain++;
             }
         }
        str2=str2.toUpperCase();
         QuestionPattern=new String[countagain];
         int j=0,end; 
         str2=str2.replaceAll("TF", "1");
         str2=str2.replaceAll("M", "0");
         str2=str2.replaceAll("T", "2");
         str2=str2.replaceAll("D", "3");
         str2=str2.replaceAll(",", "");
         str2=str2.replaceAll(" ","");
         str2=str2.trim();
         qtype=new String(str2);
      }
    }
