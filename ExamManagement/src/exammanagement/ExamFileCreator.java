        FileChoser fchose=new FileChoser();

        int returnValue = fchose.jFileChooser1.showOpenDialog(fchose);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fchose.jFileChooser1.getSelectedFile();
            jTextField11.setText(selectedFile.getAbsolutePath());
        }
